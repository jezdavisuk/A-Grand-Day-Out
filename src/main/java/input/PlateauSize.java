package input;

public class PlateauSize {

    private final int xDim; // x dimension
    private final int yDim; // y dimension

    public PlateauSize(int xDim, int yDim) {
        this.xDim = xDim;
        this.yDim = yDim;
    }

    public int getxDim() {
        return xDim;
    }

    public int getyDim() {
        return yDim;
    }
}
